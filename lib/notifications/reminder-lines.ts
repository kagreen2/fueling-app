/**
 * Get a random reminder line from a specific category.
 * Uses a simple index tracking to avoid repeats.
 */
export function getRandomReminder(
  category: 'meal' | 'checkin' | 'hydration' | 'evening'
): string {
  const lines = {
    meal: MEAL_REMINDERS,
    checkin: CHECKIN_REMINDERS,
    hydration: HYDRATION_REMINDERS,
    evening: EVENING_REMINDERS,
  }[category]

  return lines[Math.floor(Math.random() * lines.length)]
}

/**
 * Get a random reminder that hasn't been shown recently.
 * Tracks last N shown indices to avoid repeats.
 */
export function getUniqueReminder(
  category: 'meal' | 'checkin' | 'hydration' | 'evening',
  recentIndices: number[] = [],
  historySize: number = 10
): { message: string; index: number; updatedHistory: number[] } {
  const lines = {
    meal: MEAL_REMINDERS,
    checkin: CHECKIN_REMINDERS,
    hydration: HYDRATION_REMINDERS,
    evening: EVENING_REMINDERS,
  }[category]

  // Filter out recently used indices
  const available = lines
    .map((_, i) => i)
    .filter((i) => !recentIndices.includes(i))

  // If all have been used, reset history
  const pool = available.length > 0 ? available : lines.map((_, i) => i)
  const selectedIndex = pool[Math.floor(Math.random() * pool.length)]

  // Update history (keep only last N)
  const updatedHistory = [...recentIndices, selectedIndex].slice(-historySize)

  return {
    message: lines[selectedIndex],
    index: selectedIndex,
    updatedHistory,
  }
}

